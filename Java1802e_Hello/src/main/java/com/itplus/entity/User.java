package com.itplus.entity;

public class User {
	private int id;
	private String ten_kh;
	private String so_dt;
	private int tuoi;
	public User() {		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTen_kh() {
		return ten_kh;
	}
	public void setTen_kh(String ten_kh) {
		this.ten_kh = ten_kh;
	}
	public String getSo_dt() {
		return so_dt;
	}
	public void setSo_dt(String so_dt) {
		this.so_dt = so_dt;
	}
	public int getTuoi() {
		return tuoi;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	
	
	
}
